DROP TABLE WALLET;
DROP TABLE RERULES;
DROP TABLE RRRULES;
DROP TABLE REWARD_TYPE;
DROP TABLE ACTIVITY_TYPE;
DROP TABLE BRANDS;
DROP TABLE ADMIN;
DROP TABLE CUSTOMER;
DROP TABLE USERS;

CREATE TABLE USERS(  
    USERID VARCHAR2(50),  
    PASSWORD VARCHAR2(30) NOT NULL,  
    USERTYPE CHAR(1) NOT NULL,
    CONSTRAINT pk_users primary key(USERID)  
);

CREATE TABLE CUSTOMER(
	CUSTOMERID NUMBER(10),
	FNAME VARCHAR2(50),
    LNAME VARCHAR2(50),
	PHONENUMBER NUMBER(10),
    HOMEPHONE NUMBER(10),
	ADDRESS VARCHAR2(80),
    USERID VARCHAR2(50),
	CONSTRAINT pk_customer primary key(CUSTOMERID), 
    CONSTRAINT FK_CUSTOMER FOREIGN KEY(USERID) REFERENCES USERS(USERID) ON DELETE CASCADE
);



CREATE TABLE ADMIN(
	ADMINID NUMBER(10),
	FNAME VARCHAR2(50),
    LNAME VARCHAR2(50),
	PHONENUMBER NUMBER(10),
    HOMEPHONE NUMBER(10),
    ADDRESS VARCHAR2(80),
    USERID VARCHAR2(50), 
	CONSTRAINT pk_admin primary key(ADMINID), 
    CONSTRAINT FK_ADMIN FOREIGN KEY(USERID) REFERENCES USERS(USERID) ON DELETE CASCADE
);



CREATE TABLE Brands(
BID NUMBER(10), 
BNAME VARCHAR(80),
ADDRESS VARCHAR(80),
JOINDATE DATE,
USERID VARCHAR2(50), 
CONSTRAINT PK_BRAND primary key(bId), 
CONSTRAINT FK_BRAND FOREIGN KEY (USERID) REFERENCES Users(UserID)
ON DELETE CASCADE
);



CREATE TABLE ACTIVITY_TYPE(
	ACTIVITYCODE VARCHAR2(10) NOT NULL,
	ACTIVITYNAME CHAR (50),
	CONSTRAINT pk_activity primary key(ACTIVITYCODE) 
);



CREATE TABLE REWARD_TYPE(
	REWARDCODE  VARCHAR2(10) NOT NULL,
	REWARDNAME CHAR(50),
	CONSTRAINT pk_rewards primary key(REWARDCODE)
);



CREATE TABLE RRRULES(
RRCODE VARCHAR2(10),
VERSIONNO NUMBER(5),
POINTS NUMBER(10),
REWARDCODE VARCHAR2(10),
CONSTRAINT PK_RRRULES primary key(RRCODE, VERSIONNO),
CONSTRAINT FK_RRCODE FOREIGN KEY (REWARDCODE) REFERENCES REWARD_TYPE(REWARDCODE)
);



CREATE TABLE RERULES(
RECODE VARCHAR2(10),
VERSIONNO NUMBER(5),
POINTS NUMBER(10),
ACTIVITYCODE VARCHAR2(10),
CONSTRAINT PK_RERULES primary key(RECODE, VERSIONNO),
CONSTRAINT FK_ACTIVITYCODE_RE FOREIGN KEY (ACTIVITYCODE) REFERENCES ACTIVITY_TYPE(ACTIVITYCODE)
);



CREATE TABLE WALLET(
	WALLETID VARCHAR2(4) NOT NULL,
	POINTSEARNED FLOAT(10),
	DATEOFACTIVITY DATE,
	CUSTOMERID NUMBER(10), 
	CONSTRAINT pk_wallet primary key(WALLETID),
	CONSTRAINT fk_customerwallet FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMER(CUSTOMERID)
);




